		<p> <b> Novara </b> è un comune italiano di 105.574 abitanti, capoluogo dell'omonima provincia in Piemonte. È il secondo comune della regione per popolazione dopo Torino e crocevia di importanti traffici commerciali tra gli assi viari che congiungono Milano a Torino e Genova alla Svizzera.</p>
		<p>Simbolo della città è la cupola della Basilica di San Gaudenzio, alta 121 metri.</p>
			<img src="images/cupola-novara.jpg"><br>La cupola della Basilica di San Gaudenzio 
		<p> Novara dista 49 km da Milano (15 km dal confine con la regione Lombardia), 95 km da Torino, 23 km da Vercelli. Dista inoltre circa 35 km dal <a href="lago-maggiore.html"> Lago Maggiore </a> e 40 km dal Lago d'Orta.</p>
			<img src="images/risaie.jpg"><br>Risaie allagate alla periferia della città
	<p><h2> Monumenti e luoghi d'interesse </h2></p>
		<p> L'antico nucleo cittadino di Novara, di forma pressoché pentagonale, è situato su un modesto rilievo collinare (oggi Centro Storico, sede dell'omonima circoscrizione) e conserva ancora per gran parte, nonostante le pesanti manomissioni in chiave moderna e le molte architetture neoclassiche, l'antico impianto medievale con strade ciottolate e piccole piazze (piazza delle Erbe e piazza della Repubblica).</p>
			<img src="images/mura-romane.jpg"><br>Resti delle mura romane 
	<p><h2> Centro storico </h2></p>
		<p> Il monumento più celebre di Novara è la <b> Basilica di San Gaudenzio </b> , costruita tra fine Cinquecento ed inizio Seicento, e caratterizzata dall'imponente cupola neoclassica a pinnacolo progettata da Alessandro Antonelli e aggiunta al corpo della chiesa nella seconda metà XIX secolo, da molti considerata la più alta al mondo in mattoni. </p>
		<p> Centro della vita religiosa cittadina è l'imponente <b> Duomo </b> , in stile neoclassico, progettato anch'esso da Alessandro Antonelli e costruito a metà del XIX secolo sui resti di una preesistente cattedrale romanica (sorge infatti nello stesso punto in cui si trovava anticamente il tempio di Giove), della quale appartengono ancora la parte inferiore del campanile, il <b> Chiostro della Canonica </b> e <b> l'Oratorio di San Siro </b>.</p>
			<img src="images/duomo.jpg"><br>Porticato della piazza del Duomo 
	<p><h2> Architetture civili </h2></p>
		<ul>
			<li> <b> Arengo del Broletto </b> (XII-XV secolo).  </li>
			<li> <b> Palazzo Tornielli-Bellini </b> (inizio XVI secolo), dal 1905 è la sede centrale della Banca Popolare di Novara.  </li>
			<li> <b> Ospedale Maggiore della Carità </b> , edificato a partire dal 1628 ed ampliato nei secoli successivi ad opera di vari architetti tra i quali Alessandro Antonelli, a metà dell'Ottocento.  </li>
			<li> <b> Teatro Coccia </b> (fine del XIX secolo), inaugurato il 22 dicembre 1888. L'attuale edificio sorge sulle spoglie del settecentesco teatro Morelli (poi dedicato a Carlo Coccia), abbattuto per permettere la nuova costruzione, opera dell'architetto Giuseppe Oliverio. Oltre a personaggi illustri come Arturo Toscanini e il compianto Guido Cantelli, in anni più recenti ha ospitato il programma televisivo Bravo Bravissimo, condotto da Mike Bongiorno.  </li>
			<li> <b> Casa Quaroni </b> (XX secolo), testimonianza di stile liberty a Novara. </li>
